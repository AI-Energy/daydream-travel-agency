def print_itinerary(itinerary):
    """
    Prints each item in the given itinerary.

    Args:
        itinerary (list): A list of itinerary items to be printed.
    """
    for item in itinerary:
        print(item)

# Example of a redundant function
def count_itinerary_items(itinerary):
    return len(itinerary)
